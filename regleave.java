    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        
        String Employee_Id = txtno.getText();
        String name = txtname.getText();
        String category = txtcategory.getText();
        String Salary = txtsalary.getText();
        
        Connection();
        
        try {
            pst = con.prepareStatement("insert into registration(Employee_Id,Name,Category,Salary)values(?,?,?,?)");
            pst.setString(1, Employee_Id);
            pst.setString(2, name);
            pst.setString(3, category);
            pst.setString(4, Salary);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this,"Employee Added!");
            
            txtname.setText("");
            txtcategory.setText("");
            txtsalary.setText("");
            txtname.requestFocus();
            AutoID();
            
            
            
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(regleave.class.getName()).log(Level.SEVERE, null, ex);
        }
        
                
    }//GEN-LAST:event_btnsaveActionPerformed

