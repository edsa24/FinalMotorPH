    public void AutoID(){
        
        Connection();
        Statement s;
        try {
            s = con.createStatement();
            ResultSet rs = s.executeQuery("Select MAX(Employee_ID) from registration");
            rs.next();
            rs.getString("MAX(Employee_ID)");
            
            
                if (rs.getString("MAX(Employee_ID)") == null){
                    txtno.setText("10001");
                }
                else
                {
                    long id = Long.parseLong(rs.getString("MAX(Employee_ID)").substring(2,rs.getString("MAX(Employee_ID)").length()));
                    id++;
                    
                    txtno.setText("10" + String.format("%03d", id));
                }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(regleave.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }
    
    
    
    

